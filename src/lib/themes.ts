/** Make an arbitrary string safe to use as a URL slug. */
export function slugify(value: string): string {
	return value
		.toLowerCase()
		.trim()
		.normalize('NFD')
		.replace(/[̀-ͯ]/g, '')
		.replace(/[^a-z0-9]+/g, '-')
		.replace(/^-+|-+$/g, '');
}
